package ru.netology;

public class Cinema {
    private String nameCinema;
    private String genre;
    private int year;
    private int rating;

    public Cinema(String nameCinema, String genre, int year, int rating) {
        this.nameCinema = nameCinema;
        this.genre = genre;
        this.year = year;
        this.rating = rating;
    }

}
